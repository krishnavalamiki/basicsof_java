package Basics_java;

public class Statements_switch {

	public static void main(String[] args) {
		// switch case it have more then possibilities..
		
		String month = "Dec";
		
		switch(month) {
		case "jan":
			System.out.println("yes ! this month is jan");
		case "feb":
			System.out.println("yes ! this month is feb");
		case "march":
			System.out.println("yes ! this month is march");
		case "April":
			System.out.println("yes ! this month is April");
		case "may":
			System.out.println("yes ! this month is may");
		case "june":
			System.out.println("yes ! this month is june");
		case "july":
			System.out.println("yes ! this month is july");
		case "aug":
			System.out.println("yes ! this month is aug");
		case "sep":
			System.out.println("yes ! this month is sep");
		case "oct":
			System.out.println("yes ! this month is oct");
		case "nov":
			System.out.println("yes ! this month is nov");
		default:
			System.out.println("yes ! this month dec..");
		}

	}

}
