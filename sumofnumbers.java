package Basics_java;

public class sumofnumbers {

	public static void main(String[] args) {
		// sum of numbers in the given program..
	    int num = 10;
	    int sum = 0;
	    for(int i = 0; i<=num; i++) {
	    	sum = sum +i;
	    }
	    System.out.println("sum of the 1 t0 10 numbers = "+sum+"..");
	}

}
