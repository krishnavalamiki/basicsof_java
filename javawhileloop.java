package Basics_java;

public class javawhileloop {

	public static void main(String[] args) {
		// java while loop is iterate a part of several program..
	 
		/*
		 * syntax of while loop..
		 * intillization.
		 * while(condition){
		 *   increment or decrement (++),(--);
		 * }
		 * 
		 */
		  
		  int i = 0;
		  while(i<=30) {
			  System.out.println(i);  // while loop
			  i++;
		  }
	}

}
